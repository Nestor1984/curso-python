"""
La asignacion multiple nos permite asignar multiples variables al mismo tiempo usando una sola linea de codigo
"""
# Ejemplo 1:
# nombre = "Nestor"
# edad = 20
# atractivo = True
nombre, edad, atractivo = "Nestor", 20, True
print(nombre)
print(edad)
print(atractivo)

# Ejemplo 2:
